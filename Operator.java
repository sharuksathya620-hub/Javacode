public class Operator {
    public static void main(String[] args) {
        int i=3;
        int a=5;
        System.out.println(~i);
        System.out.println(i^a);
        System.out.println(i|a);
        System.out.println(a&i);
        System.out.println(32>>5);
        System.out.println(32<<5);
    }
    
}
